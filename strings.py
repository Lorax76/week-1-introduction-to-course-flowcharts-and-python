# strings.py
# This program shows how to print text and numbers,
# and explains the difference between strings and numbers

print("Hello World!")#Prints Hello World on the screen

# Saves the text "Hello World!" into a variable
my_text="Hello World!"

# Prints the text stored in the variable
print(my_text)

# Prints a sentence that includes an apostrophe
print("This is Sam's book.")

# Prints the same sentence using an escape character
print('This is Sam\'s book.')

# Prints different types of numbers
print(1) # A whole number
print(5.7) # A decimal number
print(-6.789) # A negative decimal number

# Stores a whole number in a variable
my_int=125

 #Prints the integer variable
print(my_int)

# Stores a decimal number in a variable
my_float=25.763

# Prints the float variable
print(my_float)

#Difference between the numbers and strings
print("1"+ "2")  # Joins the two strings together
print(1+2)   # Adds the two numbers`` 