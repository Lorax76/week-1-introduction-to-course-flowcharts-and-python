# This program shows how variables can store text and numbers
# and how their values can be changed in a program

print("*** This program shows the use of a variable.***") #Prints a message on the screen

# Stores the text "Hello World!" in a variable
my_text ="Hello World!"

 # Prints the text stored in the variable
print(my_text)

 # Changes the value of the variable to a new sentence
my_text="Dear Reader, how are you?"

# Prints the new value of the variable
print(my_text)

# Stores whole numbers in variables
number1=2
number2=3

# Adds the two numbers and prints the result
print(number1+number2)

 # Changes number1 to a decimal number
number1=2.0

# Adds a decimal number and a whole number
print(number1+number2)

# Stores a whole number in number1
number1=2

# Prints the type of number1 (integer)
print(type(number1))

 #Changes number1 to a decimal number
number1=2.0

# Prints the type of number1 (float)
print(type(number1))